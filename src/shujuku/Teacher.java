package shujuku;

public class Teacher {
	private int TId;
	private String Tname;
	private int count;

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public Teacher() {
	}

	public int getTId() {
		return TId;
	}

	public void setTId(int tId) {
		TId = tId;
	}

	public String getTname() {
		return Tname;
	}

	public void setTname(String tname) {
		Tname = tname;
	}
}
